class sayHi
{
    public static void main(String[] args) {
        // test cases
        String name1 = "nish";
        String name2 = "eric";
        String name3 = "gus";
        
        sayHi(name1);
        sayHi(name2);
        sayHi(name3);
    }
    
    public static void sayHi(String name1)
    {
        System.out.println("I want to say hi to " + name1 + "!");
    }
}



